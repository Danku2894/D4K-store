package com.d4k.ecommerce.modules.promotion.enums;

/**
 * Discount Type Enum
 * Loại giảm giá của coupon
 */
public enum DiscountType {
    /**
     * Giảm giá theo phần trăm (%)
     * Ví dụ: 10% discount
     */
    PERCENTAGE,
    
    /**
     * Giảm giá cố định (VND)
     * Ví dụ: 50,000 VND discount
     */
    FIXED_AMOUNT
}

